randomStuff = input('Type something here, please: ')

print('the primitive type of the variable is: ', type(randomStuff))
print('is this in uppercase?', randomStuff.isupper())
print('is this in lowercase?', randomStuff.islower())
print('is this a alphanumeric?', randomStuff.isalnum())
print('is this only alpha? ', randomStuff.isalpha())
print('is this printable? ', randomStuff.isprintable())